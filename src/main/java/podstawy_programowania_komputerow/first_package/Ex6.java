package podstawy_programowania_komputerow.first_package;

public class Ex6 {
    public static void main(String[] args) {

        for (int i = 1; i <= 12; i++) {
            for (int j = 1; j <= 12; j++) {
                System.out.println(i + " * " + j + " = " + i * j);
            }
        }
    }
}

